    public class Startup
    {
        public IWebHostEnvironment Environment { get; }

        public Startup(IWebHostEnvironment environment)
        {
            Environment = environment;
        }

        public void ConfigureServices(IServiceCollection services)
        {
            var migrationsAssembly = typeof(Startup).GetTypeInfo().Assembly.GetName().Name;
            const string connectionString = "Server=tcp:onboarding-ecom.database.windows.net,1433;Initial Catalog=onboarding-auth-2;Persist Security Info=False;User ID=onboarding;Password=P@ssw0rd1!;MultipleActiveResultSets=False;Encrypt=True;TrustServerCertificate=False;Connection Timeout=30;";

            List<TestUser> users = new List<TestUser>();
            TestUser user1 = new TestUser();
            user1.Username = "udin";
            user1.Password = "abc123";
            users.Add(user1);

            services.AddIdentityServer()
                .AddTestUsers(users)
                .AddConfigurationStore(options =>
                {
